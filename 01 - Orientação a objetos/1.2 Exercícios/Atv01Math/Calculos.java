package Atv01Math;

public class Calculos {
	
	
	public static void somar(double num1, double num2) {
		System.out.println(num1 + num2);
	}
	
	public static void subtrair(double num1, double num2) {
		System.out.println(num1 - num2);
	}
	
	public static void multiplicar(double num1, double num2) {
		System.out.println(num1 * num2);
	}
	
	public static void dividir(double num1, double num2) {
		System.out.println(num1 / num2);
	}
	
	public static void seno(double num1) {
		System.out.println(Math.sin(num1));
	}
	
	public static void coss(double num1) {
		System.out.println(Math.cos(num1));
	}
	
	public static void tangente(double num1) {
		System.out.println(Math.tan(num1));
	}
	
	public static void log(double num1) {
		System.out.println(Math.log(num1));
	}
	public static void raizQuadrada(double num1) {
		System.out.println(Math.pow(num1, num1));
	}
}